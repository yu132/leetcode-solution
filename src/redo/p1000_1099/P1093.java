    class Solution {
        public double[] sampleStats(int[] count) {
            double max = 0, min = 0, maxCount = 0, common = 0, mid = 0, mean = 0, c = 0;
            for (int i = 0; i < count.length; ++i) {
                if (count[i] != 0) {
                    min = i;
                    break;
                }
            }

            for (int i = count.length - 1; i >= 0; --i) {
                if (count[i] != 0) {
                    max = i;
                    break;
                }
            }

            for (int i = 0; i < count.length; ++i) {
                if (count[i] > maxCount) {
                    maxCount = count[i];
                    common = i;
                }
            }

            for (int i = 0; i < count.length; ++i) {
                mean += count[i] * i;
                c += count[i];
            }
            mean /= c;

            int half = (int)(c / 2);
            for (int i = 0; i < count.length; ++i) {
                half -= count[i];
                if (half < 0) {
                    mid = i;
                    break;
                } else if (half == 0 && c % 2 == 0) {
                    for (int j = i + 1; j < count.length; ++j) {
                        if (count[j] != 0) {
                            mid = (i + j) / 2.0;
                        }
                    }
                    break;
                }
            }
            return new double[] {min, max, mean, mid, common};
        }
    }


